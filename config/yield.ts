// ===== 주식 단계 수익률 공식 매개변수 =====
//
// 공식:
//   R(M) = ( μ_max − 2·μ_max / (1 + k1·M) ) + ( σ_base + σ_bonus / (1 + k2·M) ) · Z
//
//   M        : 해당 회사에 모든 팀이 투자한 시드머니 총합
//   Z        : 정규분포를 따르고 [-1, 1] 로 잘린 확률변수
//   μ_max    : 수익률 평균의 한계 — 큰 M 에서는 +μ_max, 작은 M 에서는 -μ_max 부근
//   k1       : 평균 항이 μ_max 에 수렴하는 속도 (작을수록 천천히 수렴)
//   σ_base   : 수익률 표준편차의 최소값 (큰 M 에서 유지되는 변동성)
//   σ_bonus  : 작은 M 일 때 표준편차에 더해지는 추가 변동성
//   k2       : 표준편차 항이 σ_base 에 수렴하는 속도
//
// 값을 바꾸려면 이 파일만 수정 후 git push → Vercel 이 새 값으로 재배포.
// 로컬은 즉시 반영됨.

